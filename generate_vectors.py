# 2. 从 .env.prod 中手动读取 ZHIPU_API_KEY
def get_zhipu_key():
    env_path = Path(".env.prod")
    if env_path.exists():
        with open(env_path, "r", encoding="utf-8") as f:
            for line in f:
                if line.startswith("ZHIPU_API_KEY"):
                    return line.split("=")[1].replace('"', '').strip()
    return None

async def main():
    zhipu_key = get_zhipu_key()
    if not zhipu_key:
        print("❌ 未能在 .env.prod 中找到 ZHIPU_API_KEY，请检查文件！")
        return

    print("🚀 正在连接智谱 AI 向量生成服务...")
    client = AsyncOpenAI(api_key=zhipu_key, base_url="https://open.bigmodel.cn/api/paas/v4/")
    
    vectorized_db = []
    
    for i, item in enumerate(RAW_CORPUS):
        try:
            print(f"正在向量化第 {i+1}/{len(RAW_CORPUS)} 条场景化陈述...")
            response = await client.embeddings.create(
                model="embedding-3",
                input=item["statement"]
            )
            vector = response.data[0].embedding
            vectorized_db.append({
                "text": item["statement"],
                "vector": vector
            })
        except Exception as e:
            print(f"❌ 向量化失败: {e}")
            return

    # 保存为最终的数据库文件
    with open("corpus_vectors.json", "w", encoding="utf-8") as f:
        json.dump(vectorized_db, f, ensure_ascii=False, indent=2)
        
    print("\n✅ 恭喜！`corpus_vectors.json` 向量数据库成功生成！小满记住这些了。")

if __name__ == "__main__":
    asyncio.run(main())