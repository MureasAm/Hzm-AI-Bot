def clean_timestamps(text: str) -> str:
    """
    删除场景化陈述中的时间描述杂质，并清理残留的生成痕迹。
    """
    # 1. 删除 "在直播进行到第XX分钟时" 等变体
    text = re.sub(r'在直播[^，,。]*[分钟秒][，,。]?\s*', '', text)
    
    # 2. 删除 "在直播的第X分Y秒" 等变体
    text = re.sub(r'在直播[^，,。]*分[^，,。]*秒[，,。]?\s*', '', text)
    
    # 3. 删除可能残留的句首逗号或句号
    text = re.sub(r'^[，,。]\s*', '', text)
    
    # 4. 清理多余的空格
    text = re.sub(r'\s+', ' ', text).strip()
    
    return text

